public class PrintFMethod
{
    public static void main(String[] args){
        boolean myBool = true;
        char myC    = 'A';
        String name = "Wil";
        int age = 21;
        double amount = 29.30;

        System.out.printf("Bool %b \n",myBool);
        System.out.printf("Char %c \n",myC);
        System.out.printf("String %s \n",name);
        System.out.printf("Int %d \n",age);
        System.out.printf("Double %f \n",amount);
        System.out.printf("This is a format string %d \n",123);
    }
}
